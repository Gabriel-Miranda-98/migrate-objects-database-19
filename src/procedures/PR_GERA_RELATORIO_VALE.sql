           --   vQTDE_LINHAS_AFETADAS :=INCLUIR_VALE(dados.CODIGO_EMPRESA, dados.TIPO_CONTRATO, dados.CODIGO_CONTRATO,COD_LINHA,COD_ITNERARIO , DATA_INI,DATA_FIM);
