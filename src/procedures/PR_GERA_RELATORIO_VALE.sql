    PROCEDURE PR_VERIFICA_VALE(V_CODIGO_EMPRESA CHAR, V_TIPO_CONTRATO  CHAR,  V_CPF VARCHAR2, V_TIPO_VALE CHAR,DATA_INI DATE,DATA_FIM DATE,tipo VARCHAR2 , ALT_DEFINITIVA CHAR)
