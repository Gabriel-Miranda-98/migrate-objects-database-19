
  CREATE OR REPLACE EDITIONABLE PROCEDURE "ARTERH"."DELETE_PESSOA_0900" AS
vCONTADOR NUMBER;
BEGIN
dbms_output.enable(null);
vCONTADOR :=0;
FOR C1 IN (
select * from rhpess_pessoa where codigo_empresa = '0900'
) LOOP
vCONTADOR :=vCONTADOR+1;
dbms_output.put_line('DELETE :' || vCONTADOR || C1.CODIGO);
DELETE FROM RHPESS_PESSOA WHERE CODIGO_EMPRESA = '0900'
AND CODIGO = C1.CODIGO;
IF MOD (vCONTADOR,100) = 0
THEN COMMIT;
END IF;
END LOOP;
COMMIT;
dbms_output.put_line('DELETE CONCLU√çDO COM SUCESSO');
END DELETE_PESSOA_0900;