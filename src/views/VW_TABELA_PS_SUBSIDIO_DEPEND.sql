
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "ARTERH"."VW_TABELA_PS_SUBSIDIO_DEPEND" ("ANO_MES_REF", "VALOR_SOMENTE_MEDICO", "VALOR_MEDICO_ODONTO", "VALOR_SOMENTE_ODONTO") AS 
  select VALOR_MEDICO_ODONTO.ANO_MES_REF, VALOR_SOMENTE_MEDICO.VALNUM as VALOR_SOMENTE_MEDICO, VALOR_MEDICO_ODONTO.VALNUM AS VALOR_MEDICO_ODONTO, VALOR_SOMENTE_ODONTO.VALNUM AS VALOR_SOMENTE_ODONTO
  from
(
select VPNUM.*
  from RHPESS_TABELAS VP, RHPESS_VALNUM VPNUM
 where VP.CODIGO_TABELA = VPNUM.CODIGO_TABELA
   and VPNUM.CODIGO_TABELA = '000000000008003'
   and VPNUM.NRO_COLUNA = 4
) VALOR_SOMENTE_MEDICO,
(
select VPNUM.*
  from RHPESS_TABELAS VP, RHPESS_VALNUM VPNUM
 where VP.CODIGO_TABELA = VPNUM.CODIGO_TABELA
   and VPNUM.CODIGO_TABELA = '000000000008003'
   and VPNUM.NRO_COLUNA = 5
) VALOR_MEDICO_ODONTO,
(
select VPNUM.*
  from RHPESS_TABELAS VP, RHPESS_VALNUM VPNUM
 where VP.CODIGO_TABELA = VPNUM.CODIGO_TABELA
   and VPNUM.CODIGO_TABELA = '000000000008003'
   and VPNUM.NRO_COLUNA = 6
) VALOR_SOMENTE_ODONTO
where VALOR_MEDICO_ODONTO.CODIGO_TABELA = VALOR_SOMENTE_MEDICO.CODIGO_TABELA
  and VALOR_MEDICO_ODONTO.NRO_LINHA = VALOR_SOMENTE_MEDICO.NRO_LINHA
  and VALOR_MEDICO_ODONTO.CODIGO_TABELA = VALOR_SOMENTE_ODONTO.CODIGO_TABELA
  and VALOR_MEDICO_ODONTO.NRO_LINHA = VALOR_SOMENTE_ODONTO.NRO_LINHA
  and VALOR_MEDICO_ODONTO.ANO_MES_REF = VALOR_SOMENTE_MEDICO.ANO_MES_REF
  and VALOR_MEDICO_ODONTO.ANO_MES_REF = VALOR_SOMENTE_ODONTO.ANO_MES_REF