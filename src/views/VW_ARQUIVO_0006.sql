
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "ARTERH"."VW_ARQUIVO_0006" ("ID_ARQUIVO", "CODIGO_EMPRESA_ARQUIVO", "SITUACAO_ARQUIVO", "DATA_CARGA", "NUMERO_LINHA", "SITUACAO_LINHA", "LINHA_ORIGINAL", "ANO", "MES_REFERENCIA", "INDICADOR_EMPRESA", "CODIGO_EMPRESA", "CODIGO_CONTRATO", "CPF_SERVIDOR", "TIPO_PARTICIPANTE", "PERCENT_FACULTATIVO", "PERCENT_PATROCINADO", "PERCENT_PATROCINADOR", "VALOR_APORTE_OPCIONAL") AS 
  SELECT RHPBH_ARQUIVO.ID_ARQUIVO,
    RHPBH_ARQUIVO.CODIGO_EMPRESA AS CODIGO_EMPRESA_ARQUIVO,
    RHPBH_ARQUIVO.SITUACAO       AS SITUACAO_ARQUIVO,
    RHPBH_ARQUIVO.DATA_CARGA,
    RHPBH_ARQUIVO_LINHA.NUMERO_LINHA,
    RHPBH_ARQUIVO_LINHA.SITUACAO                AS SITUACAO_LINHA,
    RHPBH_ARQUIVO_LINHA.LINHA                   AS LINHA_ORIGINAL,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 1 , 4 )   AS ANO,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 5 , 2 )   AS MES_REFERENCIA,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 7 , 2 )   AS INDICADOR_EMPRESA,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 9 ,4 )    AS CODIGO_EMPRESA,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 13 , 15 ) AS CODIGO_CONTRATO,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 28 , 11 ) AS CPF_SERVIDOR,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 39 , 1 )  AS TIPO_PARTICIPANTE,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 40 , 5 )  AS PERCENT_FACULTATIVO,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 45 , 5 )  AS PERCENT_PATROCINADO,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 50 , 5 )  AS PERCENT_PATROCINADOR ,
    SUBSTR(RHPBH_ARQUIVO_LINHA.LINHA, 55 , 8 )  AS VALOR_APORTE_OPCIONAL
  FROM RHPBH_ARQUIVO,
    RHPBH_ARQUIVO_LINHA
  WHERE RHPBH_ARQUIVO.ID_ARQUIVO = RHPBH_ARQUIVO_LINHA.ID_ARQUIVO
  AND RHPBH_ARQUIVO.TIPO_ARQUIVO = '0006'