
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "ARTERH"."VW_GIGPES_CONTRATO_MESTRE" ("CODIGO_EMPRESA", "TIPO_CONTRATO", "CODIGO_CONTRATO", "NOME", "NOME_COMPOSTO") AS 
  select  CODIGO_EMPRESA as CODIGO_EMPRESA,  TIPO_CONTRATO as TIPO_CONTRATO,
        CODIGO_CONTRATO as CODIGO_CONTRATO,  NOME_ACESSO as NOME,
        (CODIGO_CONTRATO || ' - ' || NOME_ACESSO || ' - ' || (SELECT DESCRICAO
        FROM RHPESS_TP_CONTRATO WHERE CODIGO = TIPO_CONTRATO)) AS NOME_COMPOSTO
from   RHPESS_CONTR_MEST
