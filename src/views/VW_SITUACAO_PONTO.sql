
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "PONTO_ELETRONICO"."VW_SITUACAO_PONTO" ("CODIGO_EMPRESA", "EMPRESA", "AGRUPAMENTO_EMPRESA", "CODIGO", "DESCRICAO", "VERBA_ASSOCIADA", "DATA_CRIACAO", "DATA_EXTINCAO") AS 
  select EMPRESAS.CODIGO_EMPRESA,
       EMPRESAS.EMPRESA,
       EMPRESAS.AGRUPAMENTO_EMPRESA,
       CODIGO, DESCRICAO, VERBA_ASSOCIADA, DATA_CRIACAO, DATA_EXTINCAO
  from RHPONT_SITUACAO
,
(select '0001' AS CODIGO_EMPRESA, 'PREF.MUN.BELO HORIZONTE' EMPRESA, 'ADM DIRETA' AGRUPAMENTO_EMPRESA from dual union
 select '0098' AS CODIGO_EMPRESA, 'PREF.MUN.BH CONTRATOS'   EMPRESA, 'ADM DIRETA' AGRUPAMENTO_EMPRESA from dual
) EMPRESAS
where 
RHPONT_SITUACAO.usa_apuracao = 'Z'
--RHPONT_SITUACAO.usa_apuracao = 'S'--comentei em 14/10/16
--RHPONT_SITUACAO.CODIGO = '0048'
order by CODIGO_EMPRESA, CODIGO