
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "PONTO_ELETRONICO"."VW_FOTOS" ("CODIGO_EMPRESA", "TIPO_CONTRATO", "CODIGO_CONTRATO", "NOME", "CPF", "FOTO") AS 
  (SELECT CODIGO_EMPRESA,
    TIPO_CONTRATO,
    CODIGO_CONTRATO,
    NOME,
    CPF,
    FOTO
  FROM FOTOS_SURICATO
  WHERE DT_ENVIADO_IFPONTO_SURICATO IS NULL
  )